import java.util.*;
import java.io.*;
class OECheck {
    public static void main(String args[])
    {
           int n;
            Scanner in = new Scanner(System.in);
            n = in.nextInt();
            if(n%2 == 0)
            {
                System.out.print("Even");
            }
            else
            {
                System.out.print("Odd");
            }
            
    }
}